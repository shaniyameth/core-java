class Hospital_Based {
	public static void main(String[] args) {
		byte wardNumber=8;
		short numberOfBeds=900;
		int patientId=2143;
		float patientWeight=57.6f;
		long hospitalContact=6363756506L;
		double hospitalFees=30000.15;
		char patientGrade='A';
		boolean isAdmitted=true;
		System.out.println("Byte value: "+wardNumber);
		System.out.println("Short value:"+numberOfBeds);
		System.out.println("Int value:"+patientId);
		System.out.println("Float value:"+patientWeight);
		System.out.println("Long value:"+hospitalContact);
		System.out.println("Double value: "+hospitalFees);
		System.out.println("Char value: "+patientGrade);
		System.out.println("Boolean value: "+isAdmitted);
	}
}