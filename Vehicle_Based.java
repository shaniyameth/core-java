class Vehicle_Based{
	public static void main(String[] args){
		int price=400000;
		float milage=20.2f;
		double engineCapacity=2445.23;
		boolean isElectric=false;
		char fuelType='p';
		long registrationNumber=7975106965L;
		short modelYear=2000;
		byte numberOfWheels=10;
		System.out.println("Int value:"+price);
		System.out.println("Float value:"+milage);
		System.out.println("Double value:"+engineCapacity);
		System.out.println("Boolean value:"+isElectric);
		System.out.println("Char value:"+fuelType);
		System.out.println("Long value:"+registrationNumber);
		System.out.println("Short value:"+modelYear);
		System.out.println("Byte value"+numberOfWheels);
	}
}